import React, { useState, useEffect } from "react";
import { ArrowRight, Search } from "react-feather";


const CountryFlags = () => {
  const [countries, setCountries] = useState([]);
  const [isbuttonclicked, setisbuttonclicked]= useState (false);

  const getInfo = () => {
    fetch("https://restcountries.com/v3.1/all")
      .then((response) => response.json())
      .then((data) => setCountries(data));
  };
  useEffect(() => {
    getInfo();
    
  }, []);
  return (
    <div className="mx-auto my-8 w-full lg:w-4/6 bg-slate-100 px-8 py-8 text-red-400 ">
      <h3 className="text-center py-8 text-black text-xl font-bold">
        Choose your destination
      </h3>
      <div className="bg-white px-4 sm:px-8 py-4 lg:w-11/12 mx-auto rounded">
        <div className="bg-stone-200 w-full sm:w-80 flex mx-auto rounded-xl h-8 ">
          <Search className="w-4 ml-2 mt-1 mb-4" />
          <input
            className="text-xs ml-2 py-2 w-full bg-stone-200 rounded-xl"
            placeholder="Search"
          />
        </div>
        <h6 className="text-center text-xs text-blue-700 py-5">
          Popular Destinations
        </h6>
        <div className="grid grid-cols-1 sm:grid-cols-3 gap-4">
          {countries.map((country) => (
            <div
              key={country.cca2}
              className="flex justify-between bg-white border-2 text-xs p-2 rounded w-full items-center"
            >
              <div className="flex w-full items-center">
              <img
                src={country.flags.png}
                className="w-8 h-8 mr-2 object-cover"
                alt={country.name.common}
              />
              <p className="text-sm">{country.name.common}</p>
                </div>
                <ArrowRight className="w-4" />
            </div>
          ))}
        </div>
        <div className="flex justify-center mt-5">
          <button className="px-5 text-center text-xs text-white py-1 border border-indigo-500 w-60 rounded-full bg-indigo-500" onClick={()=> setisbuttonclicked(true)}>
            Show all Supported Countries

          </button>
        </div>
      </div>
    </div>
  );
};

export default CountryFlags;





